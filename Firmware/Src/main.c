void dmx_out(int ch){
    HAL_UART_Transmit(&huart3,(uint8_t*)&ch,1,1);
}


void DMXStart(void)
{
    // Initialize the DMX Buffer
    for (int i = 0; i < DMX_MAX; i++) {
	DMXBuffer[i] = 0;
    }
}

void DMXWrite(int channel, uint8_t value)
{
    channel = (channel < 1)?1:channel;
    channel = (channel > DMX_MAX)?DMX_MAX:channel;
    value = (value > 0xff)?0xff:value;
    DMXBuffer[channel - 1] = value;
}

// Send all buffered DMX values
void DMXFlush(void)
{
    while(HAL_UART_GetState(&huart3) == HAL_UART_STATE_BUSY_TX); // Make sure uart is not busy

    // Send the break by sending a slow 0 byte
    huart3.Init.BaudRate = 125000;
    HAL_HalfDuplex_Init(&huart3);
    dmx_out(0x00);

    while(HAL_UART_GetState(&huart3) == HAL_UART_STATE_BUSY_TX); // Make sure uart is not busy

    // Now back to DMX speed: 250000baud
    huart3.Init.BaudRate = 250000;
    HAL_HalfDuplex_Init(&huart3);
    // Write start code
    dmx_out(0x00);

    // Send DMX values
    for(uint16_t channel = 0; channel < DMX_MAX; channel++)
    {
	dmx_out(DMXBuffer[channel]);
    }

}

